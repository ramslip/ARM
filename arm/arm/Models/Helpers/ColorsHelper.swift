import UIKit

class ColorsHelper: NSObject {

    class func buttonPulseColor() -> UIColor {
        return #colorLiteral(red: 0.294, green: 0.357, blue: 0.533, alpha: 1)
    }
    
    class func lightBlue() -> UIColor {
        return #colorLiteral(red: 0.929, green: 0.925, blue: 0.949, alpha: 1)
    }
    
    class func blue() -> UIColor {
        return #colorLiteral(red: 0.294, green: 0.357, blue: 0.533, alpha: 1)
    }
    
    class func red() -> UIColor {
        return #colorLiteral(red: 1, green: 0.404, blue: 0.408, alpha: 1)
    }
    
    class func orange() -> UIColor {
        return #colorLiteral(red: 1, green: 0.682, blue: 0.267, alpha: 1)
    }
    
    class func green() -> UIColor {
        return #colorLiteral(red: 0.463, green: 0.752, blue: 0.475, alpha: 1)
    }
    
    class func visitBlue() -> UIColor {
        return #colorLiteral(red: 0.294, green: 0.639, blue: 0.922, alpha: 1)
    }
}
